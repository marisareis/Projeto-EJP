    private void estadoDoJogo() {
        
        String[] options = {"Novo jogo", "Sair"};
        String mensagemPerdeuJogo = "Que pena você não acertou a palavra \n" + "RESPOSTA: " + novoJogo.getPalavraChave().toUpperCase();
        String mensagemGanhouJogo = "Parabéns você acertou a palavra";
        
        if(novoJogo.gameOver()){
            if(novoJogo.completouBoneco()){
                int option = JOptionPane.showOptionDialog(null, mensagemPerdeuJogo, "Game Over", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, "Novo Jogo");
                
                if(option == 0){
                   new Main().setVisible(true); 
                   dispose();
                } else {
                    System.exit(0);
                }
                
            } else if (novoJogo.acertouPalavra()) {
                int option2 = JOptionPane.showOptionDialog(null, mensagemGanhouJogo, "Game Over", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, "Novo Jogo");
                
                if(option2 == 0){
                   new Main().setVisible(true); 
                   dispose();
                  
                } else {
                    System.exit(0);
                }
            }
            
        }
    }
    
