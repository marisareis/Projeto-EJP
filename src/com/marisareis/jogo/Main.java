    private void guardarLetrasDigitadas(String letra){
        boolean letraJaFoiDigitada = false;
        for(int i = 0; i < letrasDigitadas.size(); i++){
            if(letrasDigitadas.get(i).equalsIgnoreCase(letra)){
                letraJaFoiDigitada = true;
            }
        }
        
        if(letraJaFoiDigitada == false){
            letrasDigitadas.add(letra);
        }
    }
    
    private String retornaLetrasDigitadas(){
        StringBuilder aux = new StringBuilder();
        String conjuntoLetras;
        
        for(int i = 0; i < letrasDigitadas.size(); i++){
            aux.append(letrasDigitadas.get(i));
            aux.append("  ");
        }
        
        conjuntoLetras = String.valueOf(aux);
        
        return conjuntoLetras;
    }
    
    
